/* Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), calcular e escrever a 
área do retângulo.  */

let base = parseFloat(prompt("Digite a base do retângulo:"))
let altura = parseFloat(prompt("Digite a altura do retângulo:"))

area = base * altura

console.log(`a área do seu retângulo é ${area}`)